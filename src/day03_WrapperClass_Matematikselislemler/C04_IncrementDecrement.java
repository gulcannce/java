package day03_WrapperClass_Matematikselislemler;

public class C04_IncrementDecrement {

    public static void main(String[] args) {

        int sayi = 20 ;
        sayi = sayi + 5;

        System.out.println(sayi ); // 25

        //sayıyı 3 artırıp yazdırın

        System.out.println(sayi += 3);
        System.out.println(sayi ); //28

        // sayının değerini 2 katına çıkartıp yazdırın

        System.out.println( sayi *= 2);    // 56


    }
}
